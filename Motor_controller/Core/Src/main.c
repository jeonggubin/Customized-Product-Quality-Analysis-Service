static void MX_USART1_UART_Init(void);

int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_TIM3_Init();
  MX_USART1_UART_Init();

  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);
  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 1500);
  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 1500);
  HAL_Delay(1000);
  HAL_UART_Transmit(&huart1, (uint8_t*)"READY\r\n", 7, 100);
  /* USER CODE END 2 */

  /* USER CODE BEGIN WHILE */
  char rx_buf[32];
  uint8_t rx_byte = 0;
  uint8_t rx_idx = 0;

  while (1)
  {
      if (HAL_UART_Receive(&huart1, &rx_byte, 1, 10) == HAL_OK) {
          if (rx_byte == '\n' || rx_byte == '\r') {
              if (rx_idx == 0) continue;
              rx_buf[rx_idx] = '\0';
              rx_idx = 0;

              if (strncmp(rx_buf, "REJECT1", 7) == 0) {
                  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 2000);
                  HAL_Delay(500);
                  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 1500);
                  HAL_UART_Transmit(&huart1, (uint8_t*)"OK1\r\n", 5, 100);

              } else if (strncmp(rx_buf, "REJECT2", 7) == 0) {
                  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 2000);
                  HAL_Delay(500);
                  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 1500);
                  HAL_UART_Transmit(&huart1, (uint8_t*)"OK2\r\n", 5, 100);
              }
          } else if (rx_idx < sizeof(rx_buf) - 1) {
              rx_buf[rx_idx++] = rx_byte;
          }
      }
  }
  /* USER CODE END WHILE */
}

